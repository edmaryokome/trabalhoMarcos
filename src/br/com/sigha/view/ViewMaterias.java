    private void jBsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBsalvarActionPerformed
        // TODO add your handling code here:
        if (ValidaDados()) {
            if ("".equals(jTidmateria.getText())) {
                CadastraMateria();
                LimpaTela();
                DesativaTela();
                AtivaNovoExcluirEditar();
                ListaMaterias();
                
            } else {
                AlteraMateria();
                LimpaTela();
                DesativaTela();
                AtivaNovoExcluirEditar();
                ListaMaterias();
            }
        }else {
            JOptionPane.showMessageDialog(null, "A Campos Obrigatorio", "Cadastro Materia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jBsalvarActionPerformed

