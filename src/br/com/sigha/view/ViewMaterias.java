    private void ListaCursos() {
        try {
            CursoDao cd = new CursoDao();
            listacurso = cd.BuscaCurso(new UnidadeLogadoBeans().getId());
            jCcurso.removeAllItems();
            for (int i = 0; i < listacurso.size(); i++) {
                jCcurso.addItem(listacurso.get(i).getNome());
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro \n" + ex, "Cadastra Materias", JOptionPane.ERROR_MESSAGE);
        }
    }

