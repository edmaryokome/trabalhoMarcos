    private void ClickUnidade() {
        for (int i = 0; i < listunidade.size(); i++) {
            if (i == jtUnidade.getSelectedRow()) {
                jtfCodigo.setText(String.valueOf(listunidade.get(i).getId()));
                jtfBairro.setText(listunidade.get(i).getBairro());
                jtfCEP.setText(listunidade.get(i).getCep());
                jtfCidade.setText(listunidade.get(i).getCidade());
                jtfComplemento.setText(listunidade.get(i).getComplemento());
                jtfEndereco.setText(listunidade.get(i).getEndereco());
                jtfFantasia.setText(listunidade.get(i).getFantasia());
                jtfFone.setText(listunidade.get(i).getTelefone());
                jtfN.setText(listunidade.get(i).getNumero());
                JTrazaosocial.setText(listunidade.get(i).getRazao());
            //    LogoEmpresa(listunidade.get(i).getLogo());
                jFcnpj.setText(listunidade.get(i).getCnpj());
            }
        }
    }

    private void SalvaUnidade() {
        UnidadeBeans unidadebeans = new UnidadeBeans();

        unidadebeans.setBairro(jtfBairro.getText());
        unidadebeans.setCep(jtfCEP.getText());
        unidadebeans.setCidade(jtfCidade.getText());
        unidadebeans.setComplemento(jtfComplemento.getText());
        unidadebeans.setEndereco(jtfEndereco.getText());
        unidadebeans.setFantasia(jtfFantasia.getText());
        unidadebeans.setTelefone(jtfFone.getText());
        unidadebeans.setNumero(jtfN.getText());
        unidadebeans.setRazao(JTrazaosocial.getText());
        unidadebeans.setCnpj(jFcnpj.getText());
        unidadebeans.setLogo("");

        try {
            UnidadeDao unidadedao = new UnidadeDao();
            unidadedao.cadastrarUnidade(unidadebeans);
            JOptionPane.showMessageDialog(null, "Registro Salvo");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao Gravar" + ex);
        }
    }

    private void ExcluirUnidade() {

        try {
            UnidadeDao unidadedao = new UnidadeDao();
            unidadedao.excluirUnidade(Integer.valueOf(jtfCodigo.getText()));
            JOptionPane.showMessageDialog(null, "Registro Excluido com Sucesso");
            BuscarUnidade();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao Excluir" + ex);
        }
    }

    private void AlteraUnidade() {
        UnidadeBeans unidadebeans = new UnidadeBeans();
        unidadebeans.setId(Integer.valueOf(jtfCodigo.getText()));
        unidadebeans.setBairro(jtfBairro.getText());
        unidadebeans.setCep(jtfCEP.getText());
        unidadebeans.setCidade(jtfCidade.getText());
        unidadebeans.setComplemento(jtfComplemento.getText());
        unidadebeans.setEndereco(jtfEndereco.getText());
        unidadebeans.setFantasia(jtfFantasia.getText());
        unidadebeans.setTelefone(jtfFone.getText());
        unidadebeans.setNumero(jtfN.getText());
        unidadebeans.setRazao(JTrazaosocial.getText());
        unidadebeans.setCnpj(jFcnpj.getText());
        unidadebeans.setLogo("");

        try {
            UnidadeDao unidadedao = new UnidadeDao();
            unidadedao.alterarUnidade(unidadebeans);
            JOptionPane.showMessageDialog(null, "Registro Alterado");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao Alterar Registro" + ex);
        }
    }

