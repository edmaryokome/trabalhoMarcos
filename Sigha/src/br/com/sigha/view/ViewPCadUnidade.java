 public void BuscarUnidade() {

        try {
            UnidadeDao unidade = new UnidadeDao();
            listunidade = unidade.consultarUnidade();
            //JOptionPane.showMessageDialog(null, "Foi0");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao Listar Unidade" + ex.toString());
        }
        ListaUnidadeTabela(listunidade);
    }

    private void ListaUnidadeTabela(List<UnidadeBeans> listunidade) {
        DefaultTableModel tabelaunidade = (DefaultTableModel) jtUnidade.getModel();
        tabelaunidade.setNumRows(0);
        for (int i = 0; i < listunidade.size(); i++) {
            tabelaunidade.addRow(new Object[]{listunidade.get(i).getId(), listunidade.get(i).getRazao(),
                listunidade.get(i).getFantasia(), listunidade.get(i).getEndereco(), listunidade.get(i).getBairro(), listunidade.get(i).getCidade()});
        }
    }

